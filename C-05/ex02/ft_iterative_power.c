int	ft_iterative_power(int nb, int power)
{
	int	count;
	int	base;

	base = nb;
	count = 0;
	if (power < 0)
		return (0);
	else if (power == 0)
		return (1);
	else if (power == 1)
		return (nb);
	while (count < power - 1)
	{
		nb = nb * base;
		count++;
	}
	return (nb);
}
