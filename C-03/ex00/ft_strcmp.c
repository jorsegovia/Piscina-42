int	ft_strcmp(char *str1, char *str2)
{
	unsigned int	c;
	unsigned int	r;

	c = 0;
	r = 0;
	while (str1[c] != '\0' || str2[c] != '\0')
	{
		if (str1[c] != str2[c])
		{
			r = str1[c] - str2[c];
			break ;
		}
		c++;
	}
	return (r);
}
