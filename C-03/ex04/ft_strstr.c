char	*ft_strstr(char *str1, char *str2)
{
	int	c1;
	int	c2;

	c1 = 0;
	c2 = 0;
	if (*str2 == '\0')
		return (str1);
	while (str1[c1])
	{
		while (str2[c2] == str1[c1 + c2])
		{
			if (str2[c2 + 1] == '\0')
			{
				return (&str1[c1]);
			}
			c2++;
		}
		c1++;
	}
	return (0);
}
